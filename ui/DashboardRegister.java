private String generateRandomUserId() {
    StringBuilder sb = new StringBuilder("ur");
    java.util.Random random = new java.util.Random();
    for (int i = 0; i < 8; i++) {
        sb.append(random.nextInt(10)); // tambahkan angka acak 0-9
    }
    return sb.toString();
}

private String generateRandomEmoneyId() {
    StringBuilder sb = new StringBuilder("em");
    java.util.Random random = new java.util.Random();
    for (int i = 0; i < 5; i++) {
        sb.append(random.nextInt(10)); // tambahkan angka acak 0-9
    }
    return sb.toString();
}

