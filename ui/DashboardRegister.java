    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed

        String usernameStr = username.getText().trim();
        String passwordStr = new String(password.getPassword()).trim();
        String emailStr = email.getText().trim();

        // Reset status
        status.setForeground(Color.RED);
        status.setText("");

        // Validasi input
        if (usernameStr.isEmpty()) {
            status.setText("Username tidak boleh kosong.");
            return;
        }

        if (usernameStr.length() < 3) {
            status.setText("Username minimal 3 karakter.");
            return;
        }

        if (passwordStr.isEmpty()) {
            status.setText("Password tidak boleh kosong.");
            return;
        }

        if (passwordStr.length() < 8) {
            status.setText("Password minimal 8 karakter.");
            return;
        }

        if (!isValidEmail(emailStr)) {
            status.setText("Format email tidak valid.");
            return;
        }

        // Cek duplikasi username dan email di database
        if (isUsernameExists(usernameStr)) {
            status.setText("Username sudah digunakan.");
            return;
        }

        if (isEmailExists(emailStr)) {
            status.setText("Email sudah terdaftar.");
            return;
        }

        if (!isValidUsername(usernameStr)) {
            status.setText("Username hanya boleh berisi huruf dan angka.");
            return;
        }

        //simpan
        if (registerUser(usernameStr, passwordStr, emailStr)) {
            status.setForeground(Color.GREEN);
            status.setText("Register Successfully\nMengarahkan ke halaman Login...");

            // Timer untuk delay 3 detik
            javax.swing.Timer timer = new javax.swing.Timer(3000, e -> {
                // Tutup form Register setelah 3 detik
                this.dispose();

                // Buka DashboardLogin
                new DashboardLogin().setVisible(true);
            });
            timer.setRepeats(false); // Hanya jalan sekali
            timer.start(); // Mulai timer

        } else {
            status.setText("Gagal mendaftarkan akun.");
        }

    }//GEN-LAST:event_btnRegisterActionPerformed
