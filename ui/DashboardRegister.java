   private boolean registerUser(String username, String password, String email) {
    Connection conn = null;
    PreparedStatement pstmtUser = null;
    PreparedStatement pstmtEmoney = null;

    try {
        conn = DBConnection.getConnection();
        if (conn == null || conn.isClosed()) {
            status.setText("Koneksi ke database gagal.");
            return false;
        }

        // Generate id user
        String generatedUserId = null;
        int maxAttempts = 100;
        for (int i = 0; i < maxAttempts; i++) {
            generatedUserId = generateRandomUserId(); // "ur" + 8 digit
            if (!isIdExists(generatedUserId)) {
                break;
            }
            if (i == maxAttempts - 1) {
                status.setText("Gagal menghasilkan ID pengguna unik.");
                return false;
            }
        }

        // Insert user
        String sqlUser = "INSERT INTO users(id, username, password, email, role) VALUES (?, ?, ?, ?, 'user')";
        pstmtUser = conn.prepareStatement(sqlUser);
        pstmtUser.setString(1, generatedUserId);
        pstmtUser.setString(2, username);
        pstmtUser.setString(3, DashboardRegister.hashPassword(password));
        pstmtUser.setString(4, email);

        int affectedRowsUser = pstmtUser.executeUpdate();
        if (affectedRowsUser <= 0) {
            return false;
        }

        // Generate emoney_id unik
        String generatedEmoneyId = null;
        for (int i = 0; i < maxAttempts; i++) {
            generatedEmoneyId = generateRandomEmoneyId(); // "em" + 5 digit
            if (!isEmoneyIdExists(generatedEmoneyId)) {
                break;
            }
            if (i == maxAttempts - 1) {
                status.setText("Gagal menghasilkan emoney_id unik.");
                return false;
            }
        }

        // Insert emoney
        String sqlEmoney = "INSERT INTO emoney(emoney_id, balance, coin, topup_status, user_id) VALUES (?, 0, 0, 'idle', ?)";
        pstmtEmoney = conn.prepareStatement(sqlEmoney);
        pstmtEmoney.setString(1, generatedEmoneyId);
        pstmtEmoney.setString(2, generatedUserId);

        int affectedRowsEmoney = pstmtEmoney.executeUpdate();
        return affectedRowsEmoney > 0;

    } catch (SQLException e) {
        System.out.println("Error saat registrasi: " + e.getMessage());
        status.setText("Gagal mendaftarkan akun: " + e.getMessage());
        return false;
    } finally {
        try {
            if (pstmtEmoney != null) pstmtEmoney.close();
            if (pstmtUser != null) pstmtUser.close();
            if (conn != null) conn.close();
        } catch (SQLException e) {
            System.out.println("Error menutup koneksi: " + e.getMessage());
        }
    }
}

