    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

    String usernameStr = username.getText().trim();
    String passwordStr = new String(password.getPassword()).trim();

    // Reset status
    status.setForeground(Color.RED);
    status.setText("");

    // Validasi input kosong
    if (usernameStr.isEmpty()) {
        status.setText("Username tidak boleh kosong.");
        return;
    }

    if (passwordStr.isEmpty()) {
        status.setText("Password tidak boleh kosong.");
        return;
    }

    // Cek kredensial dan ambil role dari database
    String[] userData = validateAndGetRole(usernameStr, passwordStr);
    if (userData != null) {
        if ("terminated".equals(userData[0])) {
            // Tampilkan pesan error karena akun ditutup
            status.setForeground(Color.RED);
            status.setText("Your account has been terminated.");
            return; // Hentikan proses login
        }

        String role = userData[1];
        String userId = userData[2]; // Ambil user id yang dikembalikan

        // Simpan sesi pengguna
        SessionManager.login(usernameStr, role, userId); // Menyimpan userId
        status.setForeground(Color.GREEN);
        status.setText("Login Successfully");

        // Tutup form setelah delay 3 detik dan arahkan ke dashboard sesuai role
        javax.swing.Timer timer = new javax.swing.Timer(3000, e -> {
            this.dispose(); // Tutup form Login

             // Arahkan ke dashboard sesuai dengan role
            if ("user".equalsIgnoreCase(role)) {
                new DashboardUser().setVisible(true); // Arahkan ke DashboardUser
            } else if ("admin".equalsIgnoreCase(role)) {
                new DashboardAdmin().setVisible(true); // Arahkan ke DashboardAdmin
            } else if ("warehouse_manager".equalsIgnoreCase(role)) {
                new DashboardWarehouse().setVisible(true); // Arahkan ke DashboardWarehouse
            } else if ("outlet_manager".equalsIgnoreCase(role)) {
                new DashboardOutlet().setVisible(true); // Arahkan ke DashboardOutlet
            }
        });
        timer.setRepeats(false);
        timer.start();

    } else {
        status.setText("Username atau Password salah.");
    }

    }//GEN-LAST:event_btnLoginActionPerformed
