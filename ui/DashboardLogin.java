// Method untuk validasi kredensial sekaligus mengambil role
private String[] validateAndGetRole(String username, String password) {
    String sql = "SELECT * FROM users WHERE username = ? AND password = ?";
    try (Connection conn = DBConnection.getConnection(); PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, username);
        pstmt.setString(2, DashboardLogin.hashPassword(password));
        ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            // Cek apakah akun ditutup (terminated_reason tidak kosong)
            String terminatedReason = rs.getString("terminated_reason");
            if (terminatedReason != null && !terminatedReason.isEmpty()) {
                // Akun ditutup
                return new String[]{"terminated", null, null}; // Tandai sebagai akun yang ditutup
            }

            String userRole = rs.getString("role");
            String userId = rs.getString("id"); // Ambil user id
            return new String[]{rs.getString("username"), userRole, userId}; // [username, role, userId]
        }
    } catch (SQLException e) {
        System.out.println("Error saat validasi kredensial: " + e.getMessage());
    }
    return null;
}

    // Method untuk hash password menggunakan SHA-256
    public static String hashPassword(String password) {
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(password.getBytes(java.nio.charset.StandardCharsets.UTF_8));
            StringBuilder hexString = new StringBuilder();

            for (byte b : hash) {
                String hex = Integer.toHexString(0xff & b);
                if (hex.length() == 1) {
                    hexString.append('0');
                }
                hexString.append(hex);
            }

            return hexString.toString();
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException(e);
        }
    }

